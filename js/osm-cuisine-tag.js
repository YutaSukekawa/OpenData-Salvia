class OSMCuisineTag {

    static getCuisineValueJa(value) {
        switch (value) {
            case 'bagel':
                return 'ベーグル';

            case 'barbecue':
                return 'バーベキュー';

            case 'bougatsa':
                return 'ブーガッツァ';

            case 'burger':
                return 'ハンバーガー';

            case 'casserole':
                return 'カセロール';

            case 'chicken':
                return 'チキン';

            case 'crepe':
                return 'クレープ';

            default:

        }
    }

}
